
var str = 'This is a String'
console.log(str)

var str1 = 'This is a "String"'
var str2 = "This is a 'String'"
console.log(str1)
console.log(str2)


var str1 = 'This is \'String\''
var str2 = 'This is \"String\"'
console.log(str1)
console.log(str2)


// for new line
var str = 'This is \n String'
console.log(str)


// for tab
var str = 'This is \t String'
console.log(str)

// for back-slash and forward-slash
var str = 'This is \\String/'
console.log(str)

var str = 'This is \r String'
console.log(str)